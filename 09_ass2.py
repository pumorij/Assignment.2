# Q9 FIND AREA 

# Area of Rectangle
def rec(l,b):
    print("Area of Rectangle is ",l*b)
rec(10,20)

# Area of Square
def squ(side):
    square=side**2
    print("Area of Square is ",square)
squ(10)

# Area of Circle
import math as M
def cir(r):
    circle=M.pi*r**2
    print("Area of Circle is ",circle)
cir(10)